#include "../../include/minishell.h"

int	ft_in_quotes(char c, int quotes[2])
{
	if (c == '\'')
		quotes[0]++;
	else
		quotes[1]++;
	if (quotes[0] % 2 != 0 || quotes[1] % 2 != 0)
		return (1);
	else if (quotes[1] % 2 == 0)
		return (0);
	else
		return (1);
}
